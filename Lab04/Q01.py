#WAP to demonstrate Slicing Operations in Strings

text = "Hello, Python!"

print("Slicing from 0 to 5:", text[0:5])

print("Slicing from 7 to the end:", text[7:])

print("Slicing from beginning to 5:", text[:5])

print("Slicing the entire string:", text[:])

print("Slicing with step of 2:", text[::2])

print("Slicing from -6 to -1:", text[-6:-1])

print("Reversed string:", text[::-1])