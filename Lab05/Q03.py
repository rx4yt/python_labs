#Print 1st 4 even numbers (use continue statement).

# Initialize variables
count = 0
even_number = 2
# Start the while loop
while count < 4:
 # Print the current even number
 print(even_number)
 
 # Move to the next even number
 even_number += 2
 
 # Increment the count and continue to the next iteration
 count += 1
