#Write a program to demonstrate basic data type in python.

num_int = 10
print("Integer:", num_int, "Type:", type(num_int))

num_float = 10.5
print("Float:", num_float, "Type:", type(num_float))

text = "Hello, Python!"
print("String:", text, "Type:", type(text))

is_python_fun = True
print("Boolean:", is_python_fun, "Type:", type(is_python_fun))

num_list = [1, 2, 3, 4, 5]
print("List:", num_list, "Type:", type(num_list))

num_tuple = (10, 20, 30)
print("Tuple:", num_tuple, "Type:", type(num_tuple))

num_set = {10, 20, 30}
print("Set:", num_set, "Type:", type(num_set))

num_dict = {"one": 1, "two": 2, "three": 3}
print("Dictionary:", num_dict, "Type:", type(num_dict))

num_complex = 2 + 3j
print("Complex Number:", num_complex, "Type:", type(num_complex))